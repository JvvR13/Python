#Pedindo ao usuário 4 números
n1 = float(input("Digite a sua Primeira Nota") )
n2 = float(input("Digite a sua Segunda Nota"))
n3 = float(input("Digite a sua Terceira Nota"))
n4 = float(input("Digite a sua Quarta Nota"))

#Os quatro números digitados pelo usuário são somados e digitados pelo número de valores digitados pelo usuário
r = (n1 + n2 + n3 + n4)/4

#A média dos valores é impressa para o usuário
print("A média aritmética é ", r)